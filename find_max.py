a=int(input("Enter a number: "))
#------------------------------------------------------------------------------------------------------#
a = str(a)
lis = [i for i in a]
print(lis)
#------------------------------------------------------------------------------------------------------#
dist = {}
#------------------------------------------------------------------------------------------------------#
for i in lis:
    dist[i]=0
print(dist)
#------------------------------------------------------------------------------------------------------#
for i in range(len(lis)):
    for j in dist:
        if(lis[i]==j):
            dist[j]+=1
#------------------------------------------------------------------------------------------------------#
print(dist)
#------------------------------------------------------------------------------------------------------#
for i in dist:
    if dist[i] == max(dist.values()):
        print("Maximum occurring digit is:", i)
